// Recursively walks an object, calling fn(pathArr, value, parent, key).
function walkLeaves(obj, path, fn) {
  for (const [k, v] of Object.entries(obj)) {
    const here = [...path, k];
    if (typeof v === "string") {
      fn(here, v, obj, k);
    } else if (v && typeof v === "object" && !Array.isArray(v)) {
      walkLeaves(v, here, fn);
    }
  }
}

function applyTranslations(rawJson, kind /* "hi" | "hiEn" */, prefix /* "[HI]" | "[HI-EN]" */) {
  const out = JSON.parse(rawJson);
  let replaced = 0;
  let fallback = 0;
  const missing = [];

  walkLeaves(out, [], (pathArr, value, parent, key) => {
    if (typeof value !== "string") return;
    const dotPath = pathArr.join(".");
    const entry = T[dotPath];

    if (value.startsWith(prefix)) {
      if (entry && entry[kind]) {
        parent[key] = entry[kind];
        replaced += 1;
      } else {
        // Strip the "[HI] " / "[HI-EN] " marker so users see English text
        // instead of a broken placeholder while translations are completed.
        parent[key] = value.replace(new RegExp(`^${prefix.replace(/[\[\]]/g, "\\$&")}\\s*`), "");
        fallback += 1;
        missing.push(dotPath);
      }
      return;
    }

    // Also apply translations to leaves whose current value matches the
    // English source — the fallback case from previous runs. This lets us
    // grow the T map incrementally without leaving stale English in place.
    if (entry && entry[kind]) {
      parent[key] = entry[kind];
      replaced += 1;
    }
  });

  return { json: out, replaced, fallback, missing };
}

function format(json) {
  return JSON.stringify(json, null, 2) + "\n";
}

